// Choice Properties — Replit Config Generator
// Generates config.js from environment variables for Replit preview/development.
// This is a simplified version of generate-config.js without Cloudflare-only checks.

const fs = require('fs');

const SUPABASE_URL      = process.env.SUPABASE_URL      || '';
const SUPABASE_ANON_KEY = process.env.SUPABASE_ANON_KEY || '';
const SITE_URL          = (process.env.SITE_URL || '').replace(/\/$/, '');
const APPLY_FORM_URL    = (process.env.APPLY_FORM_URL || 'https://apply-choice-properties.pages.dev').replace(/\/$/, '');
const GAS_URL           = (process.env.GAS_URL || 'https://script.google.com/macros/s/AKfycbwqctrCLYOPaz1nZeMS5SXuqK7FRXbN5Bf0dSx3-3leyp_B7Bfr4HPC8YZaZ9wZVxtn/exec').replace(/\/$/, '');
const IMAGEKIT_URL      = process.env.IMAGEKIT_URL        || '';
const IMAGEKIT_PUBLIC_KEY = process.env.IMAGEKIT_PUBLIC_KEY || '';
const GEOAPIFY_API_KEY  = process.env.GEOAPIFY_API_KEY   || '';
const COMPANY_NAME      = process.env.COMPANY_NAME        || 'Choice Properties';
const COMPANY_EMAIL     = process.env.COMPANY_EMAIL       || 'hello@choiceproperties.com';
const COMPANY_PHONE     = process.env.COMPANY_PHONE       || '';
const COMPANY_TAGLINE   = process.env.COMPANY_TAGLINE     || 'Your trust is our standard.';
const COMPANY_ADDRESS   = process.env.COMPANY_ADDRESS     || '';

const LEASE_DEFAULT_LATE_FEE_FLAT  = Number(process.env.LEASE_DEFAULT_LATE_FEE_FLAT)  || 50;
const LEASE_DEFAULT_LATE_FEE_DAILY = Number(process.env.LEASE_DEFAULT_LATE_FEE_DAILY) || 10;
const LEASE_DEFAULT_EXPIRY_DAYS    = Number(process.env.LEASE_DEFAULT_EXPIRY_DAYS)    || 7;

const FEATURES = {
  CO_APPLICANT:     process.env.FEATURE_CO_APPLICANT     !== 'false',
  VEHICLE_INFO:     process.env.FEATURE_VEHICLE_INFO     !== 'false',
  DOCUMENT_UPLOAD:  process.env.FEATURE_DOCUMENT_UPLOAD  !== 'false',
  MESSAGING:        process.env.FEATURE_MESSAGING        !== 'false',
  REALTIME_UPDATES: process.env.FEATURE_REALTIME_UPDATES !== 'false',
};

if (!SUPABASE_URL || !SUPABASE_ANON_KEY) {
  console.warn('⚠  SUPABASE_URL or SUPABASE_ANON_KEY not set — app will have limited functionality');
}

const output = `// ============================================================
// Choice Properties — Auto-generated config (Replit)
// Generated by generate-config-replit.js
// DO NOT EDIT THIS FILE — it is overwritten on every start
// ============================================================

const CONFIG = {
  SUPABASE_URL:      '${SUPABASE_URL}',
  SUPABASE_ANON_KEY: '${SUPABASE_ANON_KEY}',

  APPLY_FORM_URL: '${APPLY_FORM_URL}',

  IMAGEKIT_URL:        '${IMAGEKIT_URL}',
  IMAGEKIT_PUBLIC_KEY: '${IMAGEKIT_PUBLIC_KEY}',

  GEOAPIFY_API_KEY: '${GEOAPIFY_API_KEY}',

  COMPANY_NAME:     '${COMPANY_NAME}',
  COMPANY_EMAIL:    '${COMPANY_EMAIL}',
  COMPANY_PHONE:    '${COMPANY_PHONE}',
  COMPANY_TAGLINE:  '${COMPANY_TAGLINE}',
  COMPANY_ADDRESS:  '${COMPANY_ADDRESS}',

  LEASE_DEFAULT_LATE_FEE_FLAT:  ${LEASE_DEFAULT_LATE_FEE_FLAT},
  LEASE_DEFAULT_LATE_FEE_DAILY: ${LEASE_DEFAULT_LATE_FEE_DAILY},
  LEASE_DEFAULT_EXPIRY_DAYS:    ${LEASE_DEFAULT_EXPIRY_DAYS},

  FEATURES: {
    CO_APPLICANT:     ${FEATURES.CO_APPLICANT},
    VEHICLE_INFO:     ${FEATURES.VEHICLE_INFO},
    DOCUMENT_UPLOAD:  ${FEATURES.DOCUMENT_UPLOAD},
    MESSAGING:        ${FEATURES.MESSAGING},
    REALTIME_UPDATES: ${FEATURES.REALTIME_UPDATES},
  },
};

// Derived helpers
CONFIG.isLocalhost = ['localhost', '127.0.0.1'].includes(location.hostname);
CONFIG.baseUrl     = location.origin;

// ImageKit delivery helper
CONFIG.img = function(url, preset) {
  var fallback = url || '/assets/placeholder-property.jpg';
  if (!url) return '/assets/placeholder-property.jpg';
  if (!CONFIG.IMAGEKIT_URL || CONFIG.IMAGEKIT_URL.includes('YOUR_IMAGEKIT_ID')) {
    return fallback;
  }
  var transforms = {
    card:       'tr:w-600,q-80,f-webp',
    card_2x:    'tr:w-1200,q-80,f-webp',
    gallery:    'tr:w-1200,q-90,f-webp',
    gallery_2x: 'tr:w-2400,q-85,f-webp',
    strip:      'tr:w-80,h-60,c-maintain_ratio,q-70,f-webp',
    thumb:      'tr:w-120,h-120,c-maintain_ratio,q-75,f-webp',
    lightbox:   'tr:q-95,f-webp',
    og:         'tr:w-1200,h-630,c-force,fo-center,q-85,f-webp',
    avatar:     'tr:w-80,h-80,c-force,fo-face,q-80,f-webp',
    avatar_lg:  'tr:w-160,h-160,c-force,fo-face,q-85,f-webp',
  };
  var tr = transforms[preset] || transforms.gallery;
  if (url.startsWith(CONFIG.IMAGEKIT_URL)) {
    var clean = url.replace(/\\/tr:[^/]+/, '');
    return clean.replace(CONFIG.IMAGEKIT_URL, CONFIG.IMAGEKIT_URL + '/' + tr);
  }
  return url;
};

CONFIG.srcset = function(url, preset1x, preset2x) {
  var u1 = CONFIG.img(url, preset1x);
  var u2 = CONFIG.img(url, preset2x);
  if (!u1) return '';
  if (!u2 || u2 === u1) return u1;
  return u1 + ' 1x, ' + u2 + ' 2x';
};

Object.freeze(CONFIG);
Object.freeze(CONFIG.FEATURES);
`;

fs.writeFileSync('config.js', output);
console.log('✅ config.js generated for Replit');
